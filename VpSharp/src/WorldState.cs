namespace VpSharp;

/// <summary>
///     An enumeration of world states.
/// </summary>
public enum WorldState
{
    /// <summary>
    ///     The world server is online.
    /// </summary>
    Online,

    /// <summary>
    ///     The world server is stopped.
    /// </summary>
    Stopped,

    /// <summary>
    ///     The world server is in an unknown state.
    /// </summary>
    Unknown
}
